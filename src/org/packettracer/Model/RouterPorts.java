package org.packettracer.Model;

public class RouterPorts {
    private int ip;
    private int mascara;

    public RouterPorts() {
    }

    public RouterPorts(int ip, int mascara) {
        this.ip = ip;
        this.mascara = mascara;
    }

    public int getIp() {
        return ip;
    }

    public void setIp(int ip) {
        this.ip = ip;
    }

    public int getMascara() {
        return mascara;
    }

    public void setMascara(int mascara) {
        this.mascara = mascara;
    }

   
    
    
}


